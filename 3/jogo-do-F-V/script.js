function conferirViola() {
    var respViola = document.querySelector('input#respostaViola')
    var resVio = document.querySelector('div#resultadoViola')
    var resultadoVio = String(respViola.value)

    if (resultadoVio === 'VIOLA') {
        resVio.innerHTML = ` Parabéns, está correto!`
    } else {
        resVio.innerHTML = `Resposta errada, tente outra vez!`
    }
}

