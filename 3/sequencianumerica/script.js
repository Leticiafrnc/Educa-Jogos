function conferirResposta3() {
    const respResposta3 = document.querySelector('input#resposta3')
    const resResposta3 = document.querySelector('div#resultado3')
    const resultadoResposta3 = String(respResposta3.value)

    if (resultadoResposta3 === '3') {
        resResposta3.innerHTML = ` Parabéns a resposta 3, está correta! `
    } else {
        resResposta3.innerHTML = `Resposta do 1º quadrado esta errada, tente outra vez!`
    }
}

