function conferirResposta40() {
    const respResposta40 = document.querySelector('input#resposta40')
    const resResposta40 = document.querySelector('div#resultado40')
    const resultadoResposta40 = String(respResposta40.value)
    
    if (resultadoResposta40 === '40') {
        resResposta40.innerHTML = ` Parabéns a resposta 40, está correto! ` 
    } else{
        resResposta40.innerHTML = `Resposta do 1º quadrado esta errada, tente outra vez!`
    }
    } 

