function conferirSoma1() {
  const respSoma1 = document.getElementById("respSoma1").value;
  if (respSoma1 === '4') 
  {
    document.getElementById("resSoma1").innerHTML = "Parabéns! Você acertou";
  }
  else {
    document.getElementById("resSoma1").innerHTML = "Resposta Errada, tente novamente!";
  }
}
