function conferirRato() {
  const respA = document.getElementById("respA").value;
  if (respA === 'R') 
  {
    document.getElementById("respRato").innerHTML = "Parabéns! Você acertou";
  }
  else {
    document.getElementById("respRato").innerHTML = "Resposta Errada, tente novamente!";
  }
}

