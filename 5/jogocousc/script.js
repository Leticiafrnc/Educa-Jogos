function conferirPiscina() {
    const respPiscina = document.querySelector('input#respostaPiscina')
    const resPiscina = document.querySelector('div#resultadoPiscina')
    const resultadoPiscina = String(respPiscina.value)
    
    if (resultadoPiscina === 'Piscina') {
        resPiscina.innerHTML = ` Parabéns, está correto!` 
    } else{
        resPiscina.innerHTML = `Resposta errada, tente outra vez!`
    }
    } 

