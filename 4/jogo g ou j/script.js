function conferirRelogio() {
    const respRelogio = document.querySelector('input#respostaRelogio')
    const resRelogio = document.querySelector('div#resultadoRelogio')
    const resultadoRelogio = String(respRelogio.value)
    
    if (resultadoRelogio === 'Relógio') {
        resRelogio.innerHTML = ` Parabéns, está correto!` 
    } else{
        resRelogio.innerHTML = `Resposta errada, tente outra vez!`
    }
    } 

